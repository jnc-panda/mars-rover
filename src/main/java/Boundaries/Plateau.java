package Boundaries;

public class Plateau {

    private int xLength;
    private int yLength;

    public Plateau(int xLength, int yLength) {
        this.xLength = xLength;
        this.yLength = yLength;
    }

    public int get_xLength() {
        return xLength;
    }

    public int get_yLength() {
        return yLength;
    }

}
